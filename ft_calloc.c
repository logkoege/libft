#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	ptr = malloc(count * size);
	if (!ptr)
		return (0);
	if (count != 0)
	{
		ft_memset((char *)ptr, 0, count * size);
	}
	return (ptr);
}
